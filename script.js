	// gets percentile of sing for each feature
	function getPercentile(ind, val) {
		array = features[ind]
		//const percentile = (array, val) => {
		let count = 0;
		array.forEach(v => {
			if (v < val) {
			count++;
			} else if (v == val) {
			count += 0.5;
			}
		});
		return (100 * count / array.length).toFixed(0);
	}

