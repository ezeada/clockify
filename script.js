// Radial Heat Chart code adapted from blackmiaool on jsFiddle: https://jsfiddle.net/blackmiaool/7dq4a040/1/
function circularHeatChart(stroke, radius, numSegments) {
	var margin = {
		top: 20,
		right: 50,
		bottom: 50,
		left: 20
	  };
		 innerRadius = radius;
	  segmentHeight = 20;
	  accessor = function(d) {
		return d.ScaledAverage;
	  },
	  radialLabels = segmentLabels = [];
  
	function chart(selection) {
	  selection.each(function(data) {
		var svg = d3.select(this);
  
		var offset = innerRadius + Math.ceil(data.length / numSegments) * segmentHeight;
		g = svg.append("g")
		  .attr("transform", "translate(" + parseInt(margin.left + offset) + "," + parseInt(margin.top + offset) + ")");
  
		if (domain === null) {
		  domain = d3.extent(data, accessor); // (data, accessor)
		}
		var color = d3.scale.linear().domain(domain).range(range);
  
		g.selectAll("path").data(data)
		  .enter().append("path")
		  // to change opacity of whole row
		  .attr("class", function(d) {
			return "segment-" + d.Measure +" segment"
		  })
		  .attr("id", function(d) {
			var time = d.Time;
			var timeCleaned = time.split(":").join("-");
			return "segment-" + d.Measure + "-" + timeCleaned;
		  })
		  .attr("d", d3.svg.arc().innerRadius(ir).outerRadius(or).startAngle(sa).endAngle(ea))
		  .attr("stroke", function(d) {
			return stroke;
		  })
		  .attr("fill", function(d) {
			return color(accessor(d));
		  });
  
		// Unique id so that the text path defs are unique
		var id = d3.selectAll(".circular-heat")[0].length;
  
  
		//Segment labels
		var segmentLabelOffset = 7;
		var r = innerRadius + Math.ceil(data.length / numSegments) * segmentHeight + segmentLabelOffset;
		labels = svg.append("g")
		  .attr("transform", "translate(" + parseInt(margin.left + offset) + "," + parseInt(margin.top + offset) + ")");
  
		labels.append("def")
		  .append("path")
		  .attr("id", "segment-label-path-" + id)
		  .attr("d", "m0 -" + r + " a" + r + " " + r + " 0 1 1 -1 0");
  
		labels.selectAll("text")
		  .data(segmentLabels).enter()
		  .append("text")
		  .append("textPath")
		  .attr("xlink:href", "#segment-label-path-" + id)
		  .style("font-size", "12px")
		  .attr("startOffset", function(d, i) {
			return i * 100 / numSegments + 1.5 + "%";
		  })
		  .text(function(d) {
			return d;
		  });
	  });
  
	}
  
	/* Arc functions */
	ir = function(d, i) {
	  return innerRadius + Math.floor(i / numSegments) * segmentHeight;
	}
	or = function(d, i) {
	  return innerRadius + segmentHeight + Math.floor(i / numSegments) * segmentHeight;
	}
	sa = function(d, i) {
	  return (i * 2 * Math.PI) / numSegments;
	}
	ea = function(d, i) {
	  return ((i + 1) * 2 * Math.PI) / numSegments;
	}
  
	/* Configuration getters/setters */
	chart.margin = function(_) {
	  if (!arguments.length) return margin;
	  margin = _;
	  return chart;
	};
  
	chart.innerRadius = function(_) {
	  if (!arguments.length) return innerRadius;
	  innerRadius = _;
	  return chart;
	};
  
	chart.numSegments = function(_) {
	  if (!arguments.length) return numSegments;
	  numSegments = _;
	  return chart;
	};
  
	chart.segmentHeight = function(_) {
	  if (!arguments.length) return segmentHeight;
	  segmentHeight = _;
	  return chart;
	};
  
	chart.domain = function(_) {
	  if (!arguments.length) return domain;
	  domain = _;
	  return chart;
	};
  
	chart.range = function(_) {
	  if (!arguments.length) return range;
	  range = _;
	  return chart;
	};
  
	chart.radialLabels = function(_) {
	  if (!arguments.length) return radialLabels;
	  if (_ == null) _ = [];
	  radialLabels = _;
	  return chart;
	};
  
	chart.segmentLabels = function(_) {
	  if (!arguments.length) return segmentLabels;
	  if (_ == null) _ = [];
	  segmentLabels = _;
	  return chart;
	};
  
	chart.accessor = function(_) {
	  if (!arguments.length) return accessor;
	  accessor = _;
	  return chart;
	};
  
	return chart;
};
  
